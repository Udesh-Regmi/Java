// Modulus Operator in Java
public class Modulus_Operator {
    public static void main(String[] args) {
        int a = 12; 
        double b= 12.522; 
        int c = 5;
        double d = 2.5;
        System.out.println("a % c = " + a % c);
        System.out.println("b % d = " + b % d);
        System.out.println("c % a = " + c % a);
        System.out.println("d % b = " + d % b);
    }
    
}
