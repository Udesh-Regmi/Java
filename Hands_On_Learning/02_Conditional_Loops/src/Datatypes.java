public class Datatypes {
    public static void main(String[] args) {
        System.out.println("Primitives in Java");
        // Primitives are those datatypes which cannot be further divided into smaller parts.
        // Examples:
        int a =10;
        char ch= 'Z';
        boolean isLoggedIn= false;
        float marks = 89.5f;
        double pricing = 299.2213;
        long number = 232838293233892392L;


        // Non-Primitives
        // String , Array, etc which are derived  from the primitive types. all are non-primitive data types.



    }
}
